#This program calculates the perimeter and area of a rectangle.

print('Hello. I can calculate the perimeter and area of a rectangle for you.')

l = int(input('Enter the length: '))
w = int(input('Enter the width: '))

area = l*w
perimeter = l+l+w+w

print('All right, the perimeter is',perimeter,', and the area is',area,'.')


